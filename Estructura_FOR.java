
/*  El bucle for es una estructura repetitiva controlada por un contador 
 * Tiene tres partes
 * Inicializador de la variable:
 * condicion de fin del ciclo:
 * Modificacion de la variable:
*/

public class Estructura_FOR {

    public static void main(String[] args) {

        for (int contador=0; contador<=10; contador++) {
            System.out.println("Estoy dando vueltas con FOR, estoy en la vuelta " + contador);
        }
    }
}
